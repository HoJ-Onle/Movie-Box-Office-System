def radar_color_save(img, color_img_path):
    h, w = img.shape
    new_img = np.zeros((h, w, 4), dtype=np.int8)
    for i in range(h):
        for j in range(w):
            new_img[i, j] = color_map[img[i, j]]
    img = Image.fromarray(new_img, mode="RGBA")
    if not os.path.exists(os.path.dirname(color_img_path)):
        os.makedirs(os.path.dirname(color_img_path))
    img.save(color_img_path)
